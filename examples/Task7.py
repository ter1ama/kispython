def main(x):
    e1 = x & 0b1111_11
    e2 = (x >> 6) & 0b0000_00
    e3 = (x >> 12) & 0b1111_111
    e4 = (x >> 19) & 0b111
    e5 = (x >> 22) & 0b1111_1111
    e6 = (x >> 30) & 0b1111_1111_11
    result = e4 | (e5 << 3) | (e1 << 11) | (e2 << 17) | (e6 << 23) | (e3 << 32)
    return str(result)


